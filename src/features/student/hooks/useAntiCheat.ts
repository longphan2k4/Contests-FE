import { useEffect, useRef, useState, useCallback } from 'react';

// M·ªü r·ªông interface Document v√† HTMLElement ƒë·ªÉ h·ªó tr·ª£ fullscreen API
interface ExtendedDocument extends Document {
  webkitExitFullscreen?: () => Promise<void>;
  msExitFullscreen?: () => Promise<void>;
  webkitFullscreenElement?: Element;
  msFullscreenElement?: Element;
}

interface ExtendedHTMLElement extends HTMLElement {
  webkitRequestFullscreen?: () => Promise<void>;
  msRequestFullscreen?: () => Promise<void>;
}

export interface AntiCheatViolation {
  type: 'tab_switch' | 'escape_key' | 'minimize' | 'fullscreen_exit' | 'copy_paste' | 'context_menu' | 'dev_tools';
  timestamp: Date;
  description: string;
}

export interface AntiCheatConfig {
  enableFullscreen: boolean;
  enableTabSwitchDetection: boolean;
  enableCopyPasteBlocking: boolean;
  enableContextMenuBlocking: boolean;
  enableDevToolsBlocking: boolean;
  maxViolations: number;
  warningBeforeTermination: boolean;
}

const defaultConfig: AntiCheatConfig = {
  enableFullscreen: true,
  enableTabSwitchDetection: true,
  enableCopyPasteBlocking: true,
  enableContextMenuBlocking: true,
  enableDevToolsBlocking: true,
  maxViolations: 3,
  warningBeforeTermination: true,
};

export const useAntiCheat = (
  config: Partial<AntiCheatConfig> = {},
  onViolation?: (violation: AntiCheatViolation) => void,
  onTerminate?: () => void
) => {
  const finalConfig = { ...defaultConfig, ...config };
  const [violations, setViolations] = useState<AntiCheatViolation[]>([]);
  const [isFullscreen, setIsFullscreen] = useState(false);
  const [warningCount, setWarningCount] = useState(0);
  const isActive = useRef(true);

  const addViolation = useCallback((violation: AntiCheatViolation) => {
    console.log('üö® [ANTI-CHEAT] addViolation called:', violation);
    console.log('üö® [ANTI-CHEAT] isActive.current:', isActive.current);
    
    if (!isActive.current) {
      console.log('‚ö†Ô∏è [ANTI-CHEAT] isActive false, skipping violation');
      return;
    }
    
    console.log('‚úÖ [ANTI-CHEAT] Adding violation to state');
    setViolations(prev => {
      const newViolations = [...prev, violation];
      console.log('üìä [ANTI-CHEAT] Updated violations:', newViolations);
      return newViolations;
    });
    
    console.log('üìû [ANTI-CHEAT] Calling onViolation callback');
    onViolation?.(violation);
    
    setWarningCount(prev => {
      const newCount = prev + 1;
      console.log('üìä [ANTI-CHEAT] New warning count:', newCount, '/', finalConfig.maxViolations);
      
      if (newCount >= finalConfig.maxViolations) {
        console.log('üö® [ANTI-CHEAT] Max violations reached, terminating');
        if (finalConfig.warningBeforeTermination) {
          // Hi·ªÉn th·ªã c·∫£nh b√°o cu·ªëi c√πng tr∆∞·ªõc khi k·∫øt th√∫c
          setTimeout(() => {
            console.log('üö® [ANTI-CHEAT] Calling onTerminate after delay');
            onTerminate?.();
          }, 3000);
        } else {
          console.log('üö® [ANTI-CHEAT] Calling onTerminate immediately');
          onTerminate?.();
        }
      }
      return newCount;
    });
  }, [finalConfig.maxViolations, finalConfig.warningBeforeTermination, onViolation, onTerminate]);

  // Enter fullscreen
  const enterFullscreen = useCallback(async () => {
    try {
      // Ki·ªÉm tra xem c√≥ th·ªÉ request fullscreen kh√¥ng
      if (!document.fullscreenEnabled) {
        console.warn('Tr√¨nh duy·ªát kh√¥ng h·ªó tr·ª£ fullscreen');
        return false;
      }

      // Ki·ªÉm tra xem ƒë√£ ·ªü fullscreen ch∆∞a
      const extendedDoc = document as ExtendedDocument;
      const isCurrentlyFullscreen = !!(
        extendedDoc.fullscreenElement ||
        extendedDoc.webkitFullscreenElement ||
        extendedDoc.msFullscreenElement
      );

      if (isCurrentlyFullscreen) {
        setIsFullscreen(true);
        return true;
      }

      const docElement = document.documentElement as ExtendedHTMLElement;
      if (docElement.requestFullscreen) {
        await docElement.requestFullscreen();
      } else if (docElement.webkitRequestFullscreen) {
        await docElement.webkitRequestFullscreen();
      } else if (docElement.msRequestFullscreen) {
        await docElement.msRequestFullscreen();
      }
      return true;
    } catch (error) {
      console.warn('Kh√¥ng th·ªÉ v√†o ch·∫ø ƒë·ªô to√†n m√†n h√¨nh:', error);
      return false;
    }
  }, []);

  // Exit fullscreen
  const exitFullscreen = useCallback(async () => {
    try {
      const extendedDoc = document as ExtendedDocument;
      
      // Ki·ªÉm tra xem c√≥ ƒëang ·ªü fullscreen kh√¥ng
      const isCurrentlyFullscreen = !!(
        extendedDoc.fullscreenElement ||
        extendedDoc.webkitFullscreenElement ||
        extendedDoc.msFullscreenElement
      );

      if (!isCurrentlyFullscreen) {
        setIsFullscreen(false);
        return;
      }

      if (extendedDoc.exitFullscreen) {
        await extendedDoc.exitFullscreen();
      } else if (extendedDoc.webkitExitFullscreen) {
        await extendedDoc.webkitExitFullscreen();
      } else if (extendedDoc.msExitFullscreen) {
        await extendedDoc.msExitFullscreen();
      }
    } catch (error) {
      console.warn('Kh√¥ng th·ªÉ tho√°t ch·∫ø ƒë·ªô to√†n m√†n h√¨nh:', error);
    }
    setIsFullscreen(false);
  }, []);

  // Start anti-cheat monitoring
  const startMonitoring = useCallback(() => {
    console.log('üöÄ [ANTI-CHEAT] B·∫Øt ƒë·∫ßu startMonitoring');
    isActive.current = true;
    console.log('üöÄ [ANTI-CHEAT] isActive.current set to:', isActive.current);
    // Kh√¥ng t·ª± ƒë·ªông v√†o fullscreen ngay l·∫≠p t·ª©c ƒë·ªÉ tr√°nh l·ªói permissions
    // Thay v√†o ƒë√≥, ch·ªâ b√°o cho user bi·∫øt c·∫ßn v√†o fullscreen
    console.log('ƒê√£ b·∫Øt ƒë·∫ßu monitoring ch·ªëng gian l·∫≠n');
  }, []);

  // Stop anti-cheat monitoring  
  const stopMonitoring = useCallback(async () => {
    console.log('üõë [ANTI-CHEAT] B·∫Øt ƒë·∫ßu stopMonitoring');
    isActive.current = false;
    console.log('üõë [ANTI-CHEAT] isActive.current set to:', isActive.current);
    if (isFullscreen) {
      await exitFullscreen();
    }
  }, [isFullscreen, exitFullscreen]);

  useEffect(() => {
    console.log('üîÑ [ANTI-CHEAT] useEffect ƒë∆∞·ª£c g·ªçi, isActive.current:', isActive.current);
    
    if (!isActive.current) {
      console.log('‚ö†Ô∏è [ANTI-CHEAT] isActive l√† false, kh√¥ng thi·∫øt l·∫≠p event listeners');
      return;
    }

    console.log('‚úÖ [ANTI-CHEAT] Thi·∫øt l·∫≠p event listeners');

    // Ph√°t hi·ªán thay ƒë·ªïi tr·∫°ng th√°i fullscreen
    const handleFullscreenChange = () => {
      console.log('üì∫ [ANTI-CHEAT] Fullscreen change detected');
      const extendedDoc = document as ExtendedDocument;
      const isNowFullscreen = !!(
        extendedDoc.fullscreenElement ||
        extendedDoc.webkitFullscreenElement ||
        extendedDoc.msFullscreenElement
      );
      
      console.log('üì∫ [ANTI-CHEAT] isNowFullscreen:', isNowFullscreen);
      setIsFullscreen(isNowFullscreen);
      
      if (!isNowFullscreen && finalConfig.enableFullscreen) {
        console.log('‚ö†Ô∏è [ANTI-CHEAT] Fullscreen exit violation');
        addViolation({
          type: 'fullscreen_exit',
          timestamp: new Date(),
          description: 'Th√≠ sinh ƒë√£ tho√°t kh·ªèi ch·∫ø ƒë·ªô to√†n m√†n h√¨nh'
        });
      }
    };

    // Ph√°t hi·ªán chuy·ªÉn tab/c·ª≠a s·ªï (visibility change)
    const handleVisibilityChange = () => {
      console.log('üëÅÔ∏è [ANTI-CHEAT] Visibility change, document.hidden:', document.hidden);
      if (document.hidden && finalConfig.enableTabSwitchDetection) {
        console.log('‚ö†Ô∏è [ANTI-CHEAT] Tab switch violation');
        addViolation({
          type: 'tab_switch',
          timestamp: new Date(),
          description: 'Th√≠ sinh ƒë√£ chuy·ªÉn tab ho·∫∑c minimize c·ª≠a s·ªï'
        });
      }
    };

    // Ph√°t hi·ªán ph√≠m ESC v√† c√°c ph√≠m kh√°c
    const handleKeyDown = (e: KeyboardEvent) => {
      console.log('‚å®Ô∏è [ANTI-CHEAT] Key pressed:', e.key, 'ctrlKey:', e.ctrlKey, 'metaKey:', e.metaKey);
      
      // Ph√≠m ESC
      if (e.key === 'Escape') {
        console.log('‚ö†Ô∏è [ANTI-CHEAT] ESC key violation');
        e.preventDefault();
        addViolation({
          type: 'escape_key',
          timestamp: new Date(),
          description: 'Th√≠ sinh ƒë√£ nh·∫•n ph√≠m ESC'
        });
      }

      // Ch·∫∑n Copy/Paste
      if (finalConfig.enableCopyPasteBlocking) {
        if ((e.ctrlKey || e.metaKey) && (e.key === 'c' || e.key === 'v' || e.key === 'x')) {
          console.log('‚ö†Ô∏è [ANTI-CHEAT] Copy/Paste violation:', e.key);
          e.preventDefault();
          addViolation({
            type: 'copy_paste',
            timestamp: new Date(),
            description: `Th√≠ sinh ƒë√£ th·ª≠ ${e.key === 'c' ? 'copy' : e.key === 'v' ? 'paste' : 'cut'}`
          });
        }
      }

      // Ch·∫∑n F12, Ctrl+Shift+I (Developer Tools)
      if (finalConfig.enableDevToolsBlocking) {
        if (
          e.key === 'F12' ||
          (e.ctrlKey && e.shiftKey && e.key === 'I') ||
          (e.ctrlKey && e.shiftKey && e.key === 'J') ||
          (e.ctrlKey && e.key === 'U')
        ) {
          console.log('‚ö†Ô∏è [ANTI-CHEAT] DevTools violation');
          e.preventDefault();
          addViolation({
            type: 'dev_tools',
            timestamp: new Date(),
            description: 'Th√≠ sinh ƒë√£ th·ª≠ m·ªü Developer Tools'
          });
        }
      }
    };

    // Ch·∫∑n context menu (chu·ªôt ph·∫£i)
    const handleContextMenu = (e: MouseEvent) => {
      console.log('üñ±Ô∏è [ANTI-CHEAT] Context menu attempted');
      if (finalConfig.enableContextMenuBlocking) {
        console.log('‚ö†Ô∏è [ANTI-CHEAT] Context menu violation');
        e.preventDefault();
        addViolation({
          type: 'context_menu',
          timestamp: new Date(),
          description: 'Th√≠ sinh ƒë√£ th·ª≠ m·ªü context menu'
        });
      }
    };

    // Ph√°t hi·ªán blur (m·∫•t focus)
    const handleBlur = () => {
      console.log('üîç [ANTI-CHEAT] Window blur detected');
      if (finalConfig.enableTabSwitchDetection) {
        console.log('‚ö†Ô∏è [ANTI-CHEAT] Window blur violation');
        addViolation({
          type: 'minimize',
          timestamp: new Date(),
          description: 'C·ª≠a s·ªï ƒë√£ m·∫•t focus (c√≥ th·ªÉ do chuy·ªÉn ·ª©ng d·ª•ng)'
        });
      }
    };

    // Ph√°t hi·ªán orientation change (mobile)
    const handleOrientationChange = () => {
      if (finalConfig.enableTabSwitchDetection) {
        addViolation({
          type: 'minimize',
          timestamp: new Date(),
          description: 'Th√≠ sinh ƒë√£ xoay m√†n h√¨nh ho·∫∑c thay ƒë·ªïi orientation'
        });
      }
    };

    // Ph√°t hi·ªán touch events nghi ng·ªù (mobile long press)
    const handleTouchStart = (e: TouchEvent) => {
      if (finalConfig.enableContextMenuBlocking && e.touches.length > 1) {
        e.preventDefault();
        addViolation({
          type: 'context_menu',
          timestamp: new Date(),
          description: 'Th√≠ sinh ƒë√£ th·ª≠ s·ª≠ d·ª•ng multi-touch gesture'
        });
      }
    };

    // Ph√°t hi·ªán beforeunload (khi user th·ª≠ tho√°t page)
    const handleBeforeUnload = (e: BeforeUnloadEvent) => {
      e.preventDefault();
      addViolation({
        type: 'minimize',
        timestamp: new Date(),
        description: 'Th√≠ sinh ƒë√£ th·ª≠ tho√°t kh·ªèi trang thi'
      });
      e.returnValue = 'B·∫°n c√≥ ch·∫Øc mu·ªën tho√°t kh·ªèi b√†i thi?';
    };

    // Event listeners
    document.addEventListener('fullscreenchange', handleFullscreenChange);
    document.addEventListener('webkitfullscreenchange', handleFullscreenChange);
    document.addEventListener('msfullscreenchange', handleFullscreenChange);
    document.addEventListener('visibilitychange', handleVisibilityChange);
    document.addEventListener('keydown', handleKeyDown);
    document.addEventListener('contextmenu', handleContextMenu);
    document.addEventListener('touchstart', handleTouchStart, { passive: false });
    window.addEventListener('blur', handleBlur);
    window.addEventListener('orientationchange', handleOrientationChange);
    window.addEventListener('beforeunload', handleBeforeUnload);

    // Cleanup
    return () => {
      document.removeEventListener('fullscreenchange', handleFullscreenChange);
      document.removeEventListener('webkitfullscreenchange', handleFullscreenChange);
      document.removeEventListener('msfullscreenchange', handleFullscreenChange);
      document.removeEventListener('visibilitychange', handleVisibilityChange);
      document.removeEventListener('keydown', handleKeyDown);
      document.removeEventListener('contextmenu', handleContextMenu);
      document.removeEventListener('touchstart', handleTouchStart);
      window.removeEventListener('blur', handleBlur);
      window.removeEventListener('orientationchange', handleOrientationChange);
      window.removeEventListener('beforeunload', handleBeforeUnload);
    };
  }, [finalConfig, addViolation]);

  return {
    violations,
    warningCount,
    isFullscreen,
    startMonitoring,
    stopMonitoring,
    enterFullscreen,
    exitFullscreen,
    maxViolations: finalConfig.maxViolations,
    isMonitoring: isActive.current,
  };
};